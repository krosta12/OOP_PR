package org.example.ooppr.Server;

public class Mouse {
    private short x;
    private short y;

    //check % 4 ==0 here

    public Mouse(short x, short y) {
        this.x = x;
        this.y = y;
    }

    public short getX() {
        return x;
    }

    public void setX(short x) {
        this.x = x;
    }

    public short getY() {
        return y;
    }

    public void setY(short y) {
        this.y = y;
    }
}
